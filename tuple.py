#Tuple

thistuple = ("apple","banana","mango")
print(thistuple)

#access tuple items
thistuple = ("apple","banana","mango")
print(thistuple[1])

#update tuple
thistuple = ("apple","banana","mango")
thistuple.append("orange")
print(thistuple)

thistuple.remove("banana")
print(thistuple)
